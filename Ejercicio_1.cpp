#include <iostream>

int main() {

    int a;
    char b;
    float c;
    double d;
    bool e;


    std::cout << "int =" << sizeof(a) << " char = " << sizeof(b) << std::endl;
    std::cout << "float =" << sizeof(c) << " double = " << sizeof(d) << std::endl;
    std::cout << "bool =" << sizeof(e) << std::endl;
    return 0;
}


